// retrieve settings from database
// Settings are hard-coded for non-database use
function bb2_read_settings() {

	if(isset($GLOBALS['phpwcms']['bad_behavior_settings']) && is_array($GLOBALS['phpwcms']['bad_behavior_settings'])) {
		$settings = $GLOBALS['phpwcms']['bad_behavior_settings'];
	} else {
		$settings = array();
	}
	$bb2_settings_defaults = array(
		'log_table' => DB_PREPEND.'phpwcms_bad_behavior',
		'display_stats' => true,
		'strict' => false,
		'verbose' => false,
		'db_installed' => true,
		'logging' => true
	);

	return array_merge($bb2_settings_defaults, $settings);
}

